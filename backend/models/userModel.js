import mongoose from "mongoose";
import { ROLE } from "../utils/roleUtils.js";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    address: {
      type: {},
      required: true,
    },
    answer: {
      type: String,
      required: true,
    },
    role: {
      type: Number,
      enum: Object.values(ROLE),
      default: ROLE.CUSTOMER,
    },
  },
  { timestamps: true }
);

export default mongoose.model("users", userSchema);
